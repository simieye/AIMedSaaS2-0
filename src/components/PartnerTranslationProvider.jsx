export function PartnerTranslationProvider({
  children
}) {
  const [language, setLanguage] = useState('zh');
  const t = key => translations[language][key] || key;
  return <PartnerTranslationContext.Provider value={{
    t,
    language,
    setLanguage
  }}>
    {children}
  </PartnerTranslationContext.Provider>;
}
export function useTranslation() {
  const context = useContext(PartnerTranslationContext);
  if (!context) {
    throw new Error('useTranslation must be used within a PartnerTranslationProvider');
  }
  return context;
}